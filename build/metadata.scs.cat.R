
Snow crab survey by-catch data:
   
- Represent count and sometimes weight data for species caught during the annual snow crab surveys, excluding snow crab catches.

- The quality and attention given to taxonomic identification has varied throughout the years.

- Generally, the earlier the years, the poorer the quality of the identifications.

- Taxonomic groups have been identified to their highest possible levels.

- The original entries made by on-board technicians are included in the species.name.logbook column.

- Note that in the first few years of the survey, quantitative information is sometimes included in the comments, in the form offset
the number of bags, buckets or crates of catches.

